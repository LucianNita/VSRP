"---\n\n"
"## Key Design Decisions\n\n"
"### Solver-agnostic architecture\n\n"
"All experiments use `BaseSolver` as the interface. Swapping solvers requires\n"
"only changing the solver object passed to experiment functions — no model\n"
"changes needed.\n\n"
"### Promised arrival calibration\n\n"
"Container promised arrivals are calibrated to the expected initial delay\n"
"(48h) at nominal speed (20 knots). This ensures the delay constraint\n"
"correctly classifies containers as on-time or late across the delay sweep\n"
"range (0h to 108h).\n\n"
"### Swap penalty charging\n\n"
"The swap penalty is charged once per active swap group (not once per swapped\n"
"port) in both the MIP objective and the post-solve cost recomputation.\n"
"This ensures `objective_recompute_abs_gap = 0` for all instances.\n\n"
"### FuelEU penalty formula\n\n"
"The corrected energy-based formula (Article 23, FuelEU Maritime Regulation):\n\n"
"```\n"
"Penalty = max(0, g - g*) / g  *  F_total  *  penalty_rate  *  FX\n"
"```\n\n"
"where `g` = VLSFO GHG intensity (91.16 gCO2eq/MJ), `g*` = FuelEU limit,\n"
"`F_total` = total fuel burned (tonnes), `penalty_rate` = EUR 2,400 per tonne\n"
"VLSFO equivalent, `FX` = EUR/USD conversion rate.\n\n"
