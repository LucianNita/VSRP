"## Model Description\n\n"
"### Problem\n\n"
"The VSRP determines optimal recovery actions when a liner vessel faces\n"
"disruptions. Given an initial delay, the model selects:\n\n"
"- Which ports to visit or skip\n"
"- Sailing speed on each leg\n"
"- Port handling duration (standard or expedited)\n"
"- Whether to reorder port visits (swap)\n\n"
"to minimise a weighted combination of operational and service disruption costs.\n\n"
"### Objective\n\n"
"```\n"
"min  (1 - alpha) * C_op  +  alpha * C_svc\n"
"```\n\n"
"where `alpha` in [0,1] controls the service/cost trade-off.\n\n"
"**Operational cost** includes fuel, port handling, strategy penalties\n"
"(speed-up, expedited, omission, swap), port-specific penalties, and\n"
"optionally the FuelEU non-compliance penalty.\n\n"
"**Service cost** includes delay penalties and misconnection penalties.\n\n"
"### Recovery Strategies\n\n"
"| Strategy | Description |\n"
"|---|---|\n"
"| Speed adjustment | Sail faster (FAST 25kn) or slower (SLOW 15kn) than nominal (20kn) |\n"
"| Expedited handling | Reduce port-call duration from 12h to 8h |\n"
"| Port omission | Skip one or more planned port calls |\n"
"| Port swapping | Reorder two port visits (non-adjacent, Brouer et al. 2013) |\n\n"
"### Swap Implementation\n\n"
"Non-adjacent port swapping is implemented using:\n\n"
"- Three-leg swap groups (leg A: approach, leg B: reverse, leg C: continue)\n"
"- Binary ordering variables z_ij with antisymmetry and transitivity constraints\n"
"- `max_swap_distance=2` bound to prevent degenerate routes\n"
"- Swap penalty charged once per active swap group\n\n"
"### Transshipment Misconnection\n\n"
"Full Li et al. (2023) logic: a container is misconnected if the vessel\n"
"arrives at its transshipment port after `connecting_service_deadline_h`.\n"
"Non-connecting edges are identified at model-build time and constrained\n"
"to force `o[c] = 1`.\n\n"
"### Emissions and Regulatory Compliance\n\n"
"| Module | Coverage |\n"
"|---|---|\n"
"| CO2 accounting | Cubic fuel model per leg, VLSFO emission factor 3.114 tCO2/t |\n"
"| EU ETS | Phase-in schedule (40/70/100% for 2024/2025/2026+), stochastic carbon price |\n"
"| IMO CII | Attained vs required CII, A-E rating (IMO MEPC 2024 reference lines) |\n"
"| IMO EEXI | Vessel-level compliance check (speed-limit precondition) |\n"
"| FuelEU Maritime | GHG intensity limit interpolation, energy-based penalty formula |\n\n"
"**Single-fuel assumption (VLSFO):** All legs burn VLSFO at constant GHG intensity\n"
"91.16 gCO2eq/MJ. The FuelEU penalty is therefore linear in total fuel burn.\n"
"The 2026 FuelEU limit is approximately 88.61 gCO2eq/MJ (interpolated between\n"
"2025=89.34 and 2030=85.69), making VLSFO always non-compliant from 2025 onward.\n\n"
"---\n\n"
"## CFA Policy\n\n"
"Following the Powell (2022) Cost Function Approximation framework:\n\n"
"```\n"
"parametric optimization  ->  simulator  ->  parameter update  ->  repeat\n"
"```\n\n"
"**Theta:** Per-destination promise tightening vector. Higher theta at port p\n"
"forces the MIP to treat container deadlines at p as tighter, encouraging\n"
"more aggressive recovery.\n\n"
"**Uncertainty sources:**\n\n"
"| Source | Distribution |\n"
"|---|---|\n"
"| Initial delay | Truncated normal, mean=55h, std=10h |\n"
"| Port handling multipliers | Truncated normal, mean=1.0, std=0.1, per port |\n"
"| Weather speed factors | Truncated normal, mean=1.0, std=0.05, per leg |\n"
"| Carbon price | Truncated normal, mean=65, std=15 EUR/tCO2 |\n"
"| Fuel price | Truncated normal, mean=600, std=80 USD/tonne |\n\n"
"**Update policies:**\n\n"
"| Policy | Rule |\n"
"|---|---|\n"
"| Additive | theta_p += step * misses_p |\n"
"| Decay | Increase where misses occur, decrease where none |\n"
"| SPSA | Simultaneous perturbation gradient estimate with Rademacher delta |\n\n"
"**Results (20 test episodes):**\n\n"
"| Policy | Avg missed | Avg service cost | vs Baseline |\n"
"|---|---|---|---|\n"
"| Baseline | 4.00 | $298,912 | — |\n"
"| Additive | 3.60 | $272,439 | -8.9% |\n"
"| Decay | 3.70 | $277,196 | -7.3% |\n"
"| **SPSA** | **3.00** | **$243,900** | **-18.4%** |\n\n"
"SPSA achieves zero variance (p90 = p95 = mean = $243,900), demonstrating\n"
"robust convergence to a consistently better policy.\n\n"
